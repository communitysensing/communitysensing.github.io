      <h1>Deployments </h1>
      <h2>Street Sweepers</h2>
      <p><img src="images/Street-Sweeper-enclosure2-sm.jpg" alt="Street sweeper device" width="300" height="200" class="photoleft">We are collaborating with the City of San Francisco to put our air quality sensing system on street sweepers in San Francisco. The goal is to leverage mobile infrastructure to collect street-by-street readings throughout the city.  To date, the devices have been deployed on approximately a dozen street sweepers and reported GPS, carbon monoxide, ozone, NOx, temperature, and humidity data.</p>
      <h2>West Oakland Handheld Study</h2>
      <p><img src="images/headheld-device-green-sm.jpg" alt="Handheld
      device" width="300" height="200" class="photoleft">We are about
      to deploy our handheld device with
      a <a href="http://www.woeip.org/">community action group</a> in
      West Oakland, in order to learn more about carbon monoxide,
      ozone, and NOx pollutants in that area.</p>
      <h2>West Oakland Backpack Study</h2>
      <p>Particulate matter is of great interest in West Oakland, but
      no practical mobile particulate sensor is currently available.
      We are collaborating with researchers at U.C. Berkeley on the
      development of a mobile particulate matter sensor.  In the
      meantime, we are eager to try out our visualization and
      discussion software with particulate matter data, so we are
      working with a <a href="http://www.woeip.org">community action
      group</a> in West Oakland to conduct a “backpack study” with
      portable commercial particulate matter data loggers that can be
      carried in backpacks or messenger bags.</p>
      <h4>We expect to release live data from our deployments in the
